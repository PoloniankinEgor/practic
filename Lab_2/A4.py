a = int(input("Введите число a:"))
b = int(input("Введите число b:"))

# Проверяем, что оба числа в диапазоне от 1 до 1000
# Если число вне диапазона, выведем сообщение об ошибке
if not (1 <= a <= 1000 and 1 <= b <= 1000):
    print("Ошибка: числа должны быть от 1 до 1000")
else:
    result = [a, b][a <= b]
    print(result)
